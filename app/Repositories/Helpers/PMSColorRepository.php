    public function options($nullOption = [])
    {
        $options = $nullOption;
        foreach ($this->data as $entry) {
            $options[$entry->code] = $entry->caption;
        }

        return $options;
    }

    public function caption($code)
    {
        foreach ($this->data as $entry) {
            if (strtolower($entry->code) == strtolower($code)) {
                return $entry->caption;
            }
        }

        return $code;
    }

    public function autocomplete($search, $count = 5)
    {
        $results = collect($this->data);

        if ($search) {
            $results = $results->filter(function ($item) use ($search) {
                return stripos($item->code, $search) !== false || stripos($item->caption, $search) !== false;
            });
        }

        return $results->take($count);
    }
